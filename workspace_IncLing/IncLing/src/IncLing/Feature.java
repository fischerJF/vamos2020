package IncLing;

public class Feature {

	private int status;
	private FeatureType type;
	
	
	public void setStatus(int status) {
		this.status = status;
	}
	public void setType(FeatureType type) {
		this.type = type;
	}
	public int getStatus() {
		return status;
	}
	public FeatureType getType() {
		return type;
	}
	
	
}
